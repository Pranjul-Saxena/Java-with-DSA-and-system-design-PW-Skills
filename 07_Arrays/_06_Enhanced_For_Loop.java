public class _06_Enhanced_For_Loop {
    
    public static void main(String args[]){

        // In Java, the enhanced for loop (also known as the "for-each" loop) is a simplified loop construct that makes it easier to iterate through collections or arrays. It was introduced in Java 5.

        // for (type var : collection) {
        //     loop body
        // }


        int[] numbers = {1, 2, 3, 4, 5};
        
        for (int num : numbers) {
            System.out.println(num);
        }
        

    }
}
