public class _13_TypeCasting_explicit {
    

    public static void main(String args []){


        // Explicit Typecasting: Explicit typecasting, also known as narrowing, occurs when a data type of higher range is converted into a data type of lower range. Java requires explicit instructions to perform explicit typecasting. For example:

        double num1 = 10.5;
        int num2 = (int) num1; // Explicit typecasting from double to int
        System.out.println(num2); // Output: 10

    }
}
