public class _13_Loop_forLoop_In_java {
    

    public static void main(String args[]){


        // for loop: A for loop is used to iterate over a range of values a fixed number of times. The basic syntax of the for loop is:

        // for(initialization; condition; increment/decrement) {
        //     // code to be executed
        // }
        

        for(int i=1; i<=10; i++) {
            System.out.println(i);
        }
        
    }
}
