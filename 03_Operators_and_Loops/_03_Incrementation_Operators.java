public class _03_Incrementation_Operators {
    

    public static void main(String args[]){
        
        // In Java, the incrementation operator ++ is used to add 1 to a variable's current value. It can be used both as a prefix or postfix operator.

        int count = 0;
        count++; // post-increment
        System.out.println(count); // prints 1
            
        int i = 5;
        int j = ++i; // pre-increment
        System.out.println(i); // prints 6
        System.out.println(j); // prints 6
            


    }
}
