public class _15_Loop_do_whileLoop_in_java {
    

    public static void main(String args[]){

        // do-while loop: A do-while loop is similar to a while loop, but the code inside the loop is executed at least once, even if the condition is false. The basic syntax of the do-while loop is:


        // do {
        //     // code to be executed
        // } while(condition);
        

        int i = 1;
        do {
            System.out.println(i);
            i++;
        } while(i <= 10);

    }
}
